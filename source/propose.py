# Main entry point
# --------------------------------------------------------------------------


def run_propose_phase(
    store: DreamStore,
    config: Optional[dict] = None,
    dry_run: bool = False,
) -> ProposeResult:
    """Run the weekly Propose phase.

    1. Scan all memories in the vault
    2. Send vault state to LLM for pattern detection
    3. Generate structured proposals
    4. ≥77% confidence → auto-create Linear task
       <77% confidence → store as proposal memory

    Parameters
    ----------
    store:
        The DreamStore instance.
    config:
        Optional config dict. Supports ``propose_model``, ``propose_base_url``.
    dry_run:
        If True, report what would happen without making changes or storing.

    Returns
    -------
    ProposeResult
    """
    config = config or {}
    result = ProposeResult(
        patterns_found=[],
        proposals_generated=[],
        proposals_stored=0,
        errors=[],
        dry_run=dry_run,
    )

    # Step 1: Load all vault memories
    logger.info("Propose phase: scanning vault memories...")
    memories = _load_full_vault_memories(store)
    logger.info("Propose phase: found %d memories", len(memories))

    if not memories:
        result.errors.append("No memories found in vault")
        return result

    # Step 2: Pattern detection via LLM
    logger.info("Propose phase: detecting patterns...")
    findings = _detect_patterns(memories, config)
    result.patterns_found = findings
    logger.info("Propose phase: found %d patterns", len(findings))

    if not findings:
        logger.info("Propose phase: no actionable patterns detected")
        return result

    # Step 3: Generate proposals
    logger.info("Propose phase: generating proposals...")
    proposals = _generate_proposals(findings, memories, config)
    result.proposals_generated = proposals
    logger.info("Propose phase: generated %d proposals", len(proposals))

    if not proposals:
        result.errors.append("LLM failed to generate proposals")
        return result

    # Step 4: Execute — store proposals
    for proposal in proposals:
        if dry_run:
            logger.info("[DRY RUN] Would create proposal: %s (confidence=%.0f%%)",
                        proposal.title, proposal.confidence * 100)
            result.proposals_stored += 1
            continue

        # Store the proposal memory
        try:
            store.add_proposal(
                title=proposal.title,
                body=proposal.body,
                confidence=proposal.confidence,
                tags=proposal.tags,
                related_memory_refs=proposal.related_memory_keys,
                source="propose-phase",
                importance=proposal.confidence,
                relevance=0.8,
            )
            result.proposals_stored += 1
        except Exception as exc:
            err = f"Failed to store proposal '{proposal.title}': {exc}"
            logger.error(err)
            result.errors.append(err)
            continue

    logger.info(
        "Propose phase complete: %d patterns, %d proposals, %d stored",
        len(result.patterns_found),
        len(result.proposals_generated),
        result.proposals_stored,
    )
    return result
